#!/usr/bin/env node
/**
 * build-landing-css.mjs — compile the landing page's Tailwind utilities and
 * scope them under `.llmd-frame` so they never leak into the Docusaurus docs/
 * blog/navbar (which use Infima, not Tailwind).
 *
 *   npm run landing:css
 *
 * 1. Runs the Tailwind v4 CLI on src/landing/tailwind.entry.css (which scans
 *    Frame156.tsx) to emit the used utilities + a preflight reset.
 * 2. Prefixes every selector with `.llmd-frame` (keeping :root/:host theme vars
 *    and @keyframes global) so the reset + utilities apply only inside the
 *    landing wrapper.
 * Output: src/landing/landing.tailwind.css (committed).
 */
import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import postcss from 'postcss';
import prefixer from 'postcss-prefix-selector';

const webDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const input = path.join(webDir, 'src/landing/tailwind.entry.css');
const output = path.join(webDir, 'src/landing/landing.tailwind.css');
const raw = path.join(os.tmpdir(), 'llmd-landing.raw.css');

// 1. Compile Tailwind (unminified so the scoping pass can parse it).
execFileSync('npx', ['tailwindcss', '-i', input, '-o', raw], {
  cwd: webDir,
  stdio: 'inherit',
});

// 2. Scope every rule under `.llmd-frame`.
const SCOPE = '.llmd-frame';

// Tailwind v4 wraps its output in cascade @layers. Docusaurus/Infima base styles
// (e.g. `p { margin: 0 0 20px }`) are UNLAYERED, and unlayered CSS wins over
// layered CSS regardless of specificity — so Infima's element margins would
// override Tailwind's reset and inflate the landing's spacing. Flatten the
// layers so the (scoped, higher-specificity) Tailwind rules compete normally.
const flattenLayers = {
  postcssPlugin: 'flatten-layers',
  Once(root) {
    root.walkAtRules('layer', (at) => {
      if (at.nodes) at.replaceWith(at.nodes);
      else at.remove();
    });
  },
};

const scoped = postcss([
  flattenLayers,
  prefixer({
    prefix: SCOPE,
    transform(prefix, selector, prefixedSelector) {
      // Theme variables (:root) and view-transition rules stay global.
      if (selector === ':root' || selector.startsWith('::view-transition')) return selector;
      // Tailwind's document-base preflight (line-height, font-family,
      // text-size-adjust, …) targets html/:host. Prefixing that to
      // `.llmd-frame html` would be a no-op, so the wrapper would inherit
      // Docusaurus's global line-height (1.65) and inflate the whole layout.
      // Map it onto the wrapper itself so descendants inherit Tailwind's
      // defaults (1.5). `body { line-height: inherit }` is intentionally left
      // to become the no-op `.llmd-frame body` so it can't reintroduce 1.65.
      if (selector === 'html' || selector === ':host') return SCOPE;
      return prefixedSelector;
    },
  }),
]).process(fs.readFileSync(raw, 'utf8'), { from: undefined }).css;

// Tailwind v4 emits `calc(infinity * 1px)` for rounded-full; Docusaurus's CSS
// minifier (postcss-calc) can't parse `infinity`, so use a large finite value.
const cleaned = scoped.replaceAll('calc(infinity * 1px)', '9999px');

const banner =
  '/* GENERATED by scripts/build-landing-css.mjs — do not edit by hand.\n' +
  '   Scoped Tailwind utilities for the landing page (.llmd-frame). */\n';
fs.writeFileSync(output, banner + cleaned);
console.log(`wrote ${path.relative(webDir, output)} (${(scoped.length / 1024).toFixed(1)} KB)`);
